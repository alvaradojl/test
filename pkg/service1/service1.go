// Package service1 is like a calculator
package service1

// Add adds two numbers and returns another number
func Add(num1 int, num2 int) (int, error) {
	return num1 + num2, nil
}

// Subtract subtracts two numbers and returns another number
func Subtract(num1 int, num2 int) (int, error) {
	return num1 + num2, nil
}
